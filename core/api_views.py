# Brand ViewSet
# ---------------------------------------------------------------------------

class BrandViewSet(viewsets.ModelViewSet):
    """Full CRUD for Brand objects plus /extract/ and /transform/ custom actions.

    Standard routes (auto-generated by DefaultRouter):
        GET    /api/brands/          — list
        POST   /api/brands/          — create
        GET    /api/brands/{id}/     — retrieve
        PATCH  /api/brands/{id}/     — partial update (name / description only)
        DELETE /api/brands/{id}/     — destroy (cascades to documents)

    Custom actions:
        POST /api/brands/{id}/extract/   — extract tone-of-voice signature via Claude
        POST /api/brands/{id}/transform/ — rewrite text in the brand's voice
    """

    queryset = Brand.objects.all()
    serializer_class = BrandSerializer
    http_method_names = ["get", "post", "patch", "delete", "head", "options"]

    @extend_schema(
        request=None,
        responses={
            200: OpenApiResponse(description="Signature extracted successfully"),
            400: OpenApiResponse(description="No documents uploaded for this brand"),
            502: OpenApiResponse(description="Claude API failure"),
        },
        summary="Extract tone-of-voice signature from uploaded documents",
    )
    @action(detail=True, methods=["post"], url_path="extract")
    def extract(self, request: Request, pk: int = None) -> Response:
        """Analyze all uploaded documents with Claude and save the tone-of-voice signature.

        Request body: empty ``{}``.
        Returns 400 if no documents exist, 502 on Claude API failure.
        """
        brand = self.get_object()
        documents = list(brand.documents.all())

        if not documents:
            return Response(
                {"error": "No documents found for this brand. Upload at least one document before extracting."},
                status=status.HTTP_400_BAD_REQUEST,
            )

        previous_existed = brand.signature is not None
        texts = [doc.extracted_text for doc in documents]
        truncated_count = sum(1 for doc in documents if doc.truncated)

        try:
            signature = extract_signature(texts)
        except ClaudeServiceError as exc:
            return Response({"error": str(exc)}, status=status.HTTP_502_BAD_GATEWAY)

        brand.signature = signature
        brand.save(update_fields=["signature"])

        return Response({
            "brand_id": brand.pk,
            "previous_signature_existed": previous_existed,
            "signature": signature,
            "documents_analyzed": len(documents),
            "documents_truncated": truncated_count,
        })

    @extend_schema(
        request={"application/json": {"type": "object", "properties": {"text": {"type": "string"}}, "required": ["text"]}},
        responses={
            200: OpenApiResponse(description="Text transformed successfully"),
            400: OpenApiResponse(description="Missing text field or no signature extracted yet"),
            502: OpenApiResponse(description="Claude API failure"),
        },
        summary="Rewrite text in the brand's tone-of-voice",
    )
    @action(detail=True, methods=["post"], url_path="transform")
    def transform(self, request: Request, pk: int = None) -> Response:
        """Rewrite user-supplied text applying the brand's stored tone-of-voice signature.

        Request body: ``{"text": "..."}``
        Returns 400 if no signature exists or ``text`` is missing, 502 on Claude failure.
        """
        brand = self.get_object()

        if not brand.signature:
            return Response(
                {"error": "No signature found for this brand. Run POST /extract/ first."},
                status=status.HTTP_400_BAD_REQUEST,
            )

        text = (request.data.get("text") or "").strip()
        if not text:
            return Response(
                {"error": "Field 'text' is required."},
                status=status.HTTP_400_BAD_REQUEST,
            )

        try:
            transformed = transform_text(text, brand.signature)
        except ClaudeServiceError as exc:
            return Response({"error": str(exc)}, status=status.HTTP_502_BAD_GATEWAY)

        return Response({
            "brand_id": brand.pk,
            "original": text,
            "transformed": transformed,
            "original_char_count": len(text),
            "transformed_char_count": len(transformed),
            "signature_used": brand.signature,
        })


