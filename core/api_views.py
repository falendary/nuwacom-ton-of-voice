# Document ViewSet (nested under brands)
# ---------------------------------------------------------------------------

class DocumentViewSet(viewsets.ViewSet):
    """List, upload, and delete documents for a brand.

    Routes (registered manually in urls.py):
        GET    /api/brands/{brand_id}/documents/          — list
        POST   /api/brands/{brand_id}/documents/          — upload (multipart/form-data)
        DELETE /api/brands/{brand_id}/documents/{id}/     — destroy
    """

    parser_classes = [MultiPartParser, JSONParser]

    def list(self, request: Request, brand_id: int) -> Response:
        """List all documents uploaded for a brand.

        Returns 200 with a list of document objects, 404 if brand not found.
        """
        brand = get_object_or_404(Brand, pk=brand_id)
        docs = brand.documents.all()
        return Response(DocumentSerializer(docs, many=True).data)

    def create(self, request: Request, brand_id: int) -> Response:
        """Upload a document and extract its text immediately.

        Send as ``multipart/form-data`` with a ``file`` field.
        Accepted types: pdf, docx, txt, png. Max size: 20 MB.
        Returns 400 for invalid type, oversized file, or unreadable content.
        """
        brand = get_object_or_404(Brand, pk=brand_id)

        uploaded = request.FILES.get("file")
        if not uploaded:
            return Response({"error": "No file provided."}, status=status.HTTP_400_BAD_REQUEST)

        ext, error = _validate_file(uploaded)
        if error:
            return Response({"error": error}, status=status.HTTP_400_BAD_REQUEST)

        try:
            extracted, was_truncated = extract_text(uploaded, ext)
        except ValueError as exc:
            return Response({"error": str(exc)}, status=status.HTTP_400_BAD_REQUEST)

        uploaded.seek(0)
        doc = Document.objects.create(
            brand=brand,
            file=uploaded,
            filename=uploaded.name,
            file_type=ext,
            extracted_text=extracted,
            truncated=was_truncated,
        )

        return Response(DocumentSerializer(doc).data, status=status.HTTP_201_CREATED)

    def destroy(self, request: Request, brand_id: int, pk: int = None) -> Response:
        """Delete a document. Does not trigger re-extraction automatically.

        Returns 204 on success, 404 if not found.
        """
        brand = get_object_or_404(Brand, pk=brand_id)
        doc = get_object_or_404(Document, pk=pk, brand=brand)
        doc.delete()
        return Response(status=status.HTTP_204_NO_CONTENT)
